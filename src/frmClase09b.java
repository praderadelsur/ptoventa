    private void sbSetearClientes(){
        String sQuery = "";
        sQuery = "Select * from 12tblClientes";

        //Ejecuta el Query
        oConn.FnBoolQueryExecute(sQuery);
        
        // Capturo el Error
        try {

            // Verifico que haya habido resultados
            while (oConn.setResult.next())
            {
        
                // Obtengo los datos en el Objeto combo
                C.addItem(oConn.setResult.getString("intClienteCodigo") + " - " + oConn.setResult.getString("strClienteNombre") + " " + oConn.setResult.getString("strClienteApellido") );
                System.err.println(oConn.setResult.getString("intClienteCodigo") + " - " + oConn.setResult.getString("strClienteNombre") + " " + oConn.setResult.getString("strClienteApellido") );
            }
            
        // Cierro los Resultados
        oConn.setResult.close();
                
        } catch (SQLException ex) {
            oFunc.SubSistemaMensaje("sbSetearClientes:"+ex.getMessage().toString());
        }
    }
    
    private boolean fnBoolCobroCorrecto()
    {
        // Variable para el resultado
        boolean bResult = true;
        
          sbCalculaCambio();
        return bResult;
    }
    
    
    
