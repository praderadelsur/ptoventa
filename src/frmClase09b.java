    private void sbCalculaCambio()
    {
        double douPagar;
        double douRecibida;
        double  douCambio;
        
        if (! lblCobrarView.getText().isEmpty() && ! txtRecibida.getText().isEmpty())
        {

            // Obtengo la Cantidad a Pagar
            douPagar = Float.valueOf(lblCobrarView.getText());

            // Obtengo la Cantidad Recibida
            douRecibida = Float.valueOf(txtRecibida.getText());

            // Calculo la diferencia hasta que sea mayor o igual la cantidad recibida
            if (douRecibida >= douPagar)
            {
                // Calculo el Cambio
                douCambio = douRecibida-douPagar;

                // Coloca el cambio
                if (douCambio>9)
                   lblCambioView.setText(String.format("%5.2f",douCambio));            
                else
                   lblCambioView.setText(String.format("%1.2f",douCambio));             
                
            }
            else
                // Coloca el cambio en 0
                lblCambioView.setText("0.00");            
                
        
        }
    }

