    // Carga el puerto existentes a la lista
    private void SubProcesoCarga()
    {
        // Para instrucci√≥n sql
        String sqlStmt;
        String strPuerto = "";
        
         // Prepara Variable para realizar el Query
        sqlStmt = "Select * from 16tblconfigarchivoimportar";
        
        if (oConn.FnBoolQueryExecute(sqlStmt))
        {
            try 
            {
                // Verifica resultados
                 while (oConn.setResult.next())
                 {
                     // Obtiene los datos de la Consulta
                     strPuerto = oConn.setResult.getString ("strRuta");
                 }                 
                  
                 txtRuta.setText(strPuerto);
                 
                 
                 
                 // Cierra Resultados
                 oConn.setResult.close();
            } 
            catch (SQLException ex) 
            {
                JOptionPane.showMessageDialog(rootPane,ex);
                Logger.getLogger(frmClase05.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }
    
    // Valida que los datos sean capturador
    boolean FnBoolValidaDatos()
    {
        // Variable para el Mensaje
        String strMensaje="";
        
        // Valida el Ide
        if (txtRuta.getText().length()==0 )
        {
           strMensaje = "Debe completar la ruta del archivo csv.";
           txtRuta.requestFocus();
        }
                
        // Valida si faltaron datos
        if (strMensaje.length()>0)
        {
            // Construye el Mensaje
            strMensaje ="Debe ingresar los siguientes datos:\n"+strMensaje;
            
            // Muestra el Mensaje
            JOptionPane.showMessageDialog(this, strMensaje);
            
            // Retorna
            return false;
            
        }
        else
            
            return true;
        
    }
    
